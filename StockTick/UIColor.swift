import UIKit

extension UIColor {
    class func applicationColor() -> UIColor {
        return UIColor(red: 115.0/255.0, green: 35.0/255.0, blue: 220.0/255.0, alpha: 1.0)
    }
    class func applicationYellowColor() -> UIColor {
        return UIColor(red: 239.0/255.0, green: 197.0/255.0, blue: 2.0/255.0, alpha: 1.0)
    }
    class func applicationGreenColor() -> UIColor {
        return UIColor(red: 25.0/255.0, green: 138.0/255.0, blue: 63.0/255.0, alpha: 1.0)
    }
    class func applicationMaroonColor() -> UIColor {
        return UIColor(red: 186.0/255.0, green: 55.0/255.0, blue: 32.0/255.0, alpha: 1.0)
    }
    class func applicationLightGrayColor() -> UIColor {
        return UIColor(red: 237.0/255.0, green: 237.0/255.0, blue: 237.0/255.0, alpha: 1.0)
    }
}
